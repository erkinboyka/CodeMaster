function currentLang()
{
    global $lang;
    return $lang;
}

function langUrl($targetLang)
{
    $params = $_GET;
    $params['lang'] = $targetLang;
    return '?' . http_build_query($params);
}

function i18nJs()
{
    global $I18N, $lang;
    return $I18N[$lang] ?? [];
}
