function t($key, $default = null)
{
    global $I18N, $lang;
    if (isset($I18N[$lang][$key])) {
        $value = $I18N[$lang][$key];
        return function_exists('normalizeMojibakeText') ? normalizeMojibakeText((string) $value) : $value;
    }
    $fallback = $default ?? $key;
    return function_exists('normalizeMojibakeText') ? normalizeMojibakeText((string) $fallback) : $fallback;
}

